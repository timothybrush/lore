/// Identifier of a node within a revision tree.
pub type NodeID = u32;

pub const ROOT_NODE: NodeID = 0;
pub const INVALID_NODE: NodeID = 0xFFFFFFFF;
pub const INVALID_BLOCK: u32 = 0xFFFFFFFF;

pub fn node_to_file_metadata(node: NodeID) -> u32 {
    node
}

pub trait NodeIDExt {
    fn is_valid_node_id(&self) -> bool;
    fn is_valid_or_root_node_id(&self) -> bool;
}

impl NodeIDExt for NodeID {
    fn is_valid_node_id(&self) -> bool {
        *self != ROOT_NODE && *self != INVALID_NODE
    }

    fn is_valid_or_root_node_id(&self) -> bool {
        *self != INVALID_NODE
    }
}

/// Number of bits to shift a full index to get block index
const BLOCK_NODE_SHIFT: u32 = 9;
/// Number of nodes in a block
pub const BLOCK_NODE_COUNT: usize = 512;
/// Bitmask for node index in a full index
const NODE_INDEX_MASK: u32 = (1u32 << BLOCK_NODE_SHIFT) - 1;
/// Maximum size of the per-block node name buffer. Each [`NodeBlock`] holds up
/// to [`BLOCK_NODE_COUNT`] nodes, and the total concatenated name data is
/// capped at this size (4 MiB = ~8 KiB per node, far larger than any
/// legitimate path component). Rejecting further appends prevents a single
/// block from consuming unbounded memory, and bounds the allocation on
/// deserialize.
pub const NODE_NAME_MAX_SIZE: usize = 4 * 1024 * 1024;

