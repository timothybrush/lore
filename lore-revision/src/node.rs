#[error_set]
pub enum NodeNameError {
    InvalidArguments,
    Oversized,
}

fn validate_node_name(name: &str) -> Result<(), NodeNameError> {
    if name == ".." || name.starts_with('\0') || name.bytes().any(|b| b == b'/' || b == b'\\') {
        return Err(InvalidArguments {
            reason: format!("invalid node name: {name}"),
        }
        .into());
    }
    Ok(())
}

/// Owned node name backed by an arc read lock on the containing node block.
/// Since this holds a read lock on the node block, make sure to drop it as soon as possible.
/// Dereferences to `&str` for zero-copy name access. This type is `Send`
/// because it uses an `ArcRwLockReadGuard` rather than a borrowed guard.
pub struct NodeNameLock {
    // Safety invariant: `name` points into `guard.name[offset..offset+len]` and is
    // kept valid by the guard holding the arc read lock on the block data.
    // The guard must be declared after `name` so it is dropped last.
    name: *const str,
    _guard: parking_lot::ArcRwLockReadGuard<parking_lot::RawRwLock, NodeBlockRuntimeData>,
}

impl NodeNameLock {
    pub fn freeze(self) -> String {
        self.deref().to_string()
    }
}

impl AsRef<str> for NodeNameLock {
    fn as_ref(&self) -> &str {
        self
    }
}

impl AsRef<std::path::Path> for NodeNameLock {
    fn as_ref(&self) -> &std::path::Path {
        std::path::Path::new(&**self)
    }
}

impl std::ops::Deref for NodeNameLock {
    type Target = str;
    fn deref(&self) -> &str {
        // SAFETY: `name` is a valid pointer into the guard's name table,
        // which is kept alive by `_guard`.
        unsafe { &*self.name }
    }
}

impl std::fmt::Display for NodeNameLock {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_str(self)
    }
}

// SAFETY: NodeNameLock contains a *const str pointing into the ArcRwLockReadGuard's
// name table buffer. The guard keeps the data alive and prevents writers. The raw
// pointer is !Send by default, but moving NodeNameLock between threads is safe because
// the ArcRwLockReadGuard owns an Arc reference to the block, ensuring the pointed-to
// data remains valid. The !Sync BytesMut is only read through the pointer, never
// modified, dropped, or cloned.
unsafe impl Send for NodeNameLock {}

impl NodeBlock {
    pub fn new(data: Box<NodeBlockData>) -> Self {
        NodeBlock {
            data: Arc::new(RwLock::new(NodeBlockRuntimeData {
                data,
                name: BytesMut::new(),
            })),
            name_deserialized: AtomicBool::new(false),
        }
    }

    pub fn new_with_name(data: Box<NodeBlockData>, name: BytesMut) -> Self {
        NodeBlock {
            data: Arc::new(RwLock::new(NodeBlockRuntimeData { data, name })),
            name_deserialized: AtomicBool::new(true),
        }
    }

    pub fn new_zeroed() -> Self {
        let mut data = NodeBlockData::new_from_heap_zeroed();
        data.block_unused_next = INVALID_BLOCK;
        data.version = NodeBlockFormat::Nametable as u32;
        Self::new_with_name(data, BytesMut::new())
    }

    /// Get the block index from a full node ID
    pub fn index(node: NodeID) -> usize {
        (node >> BLOCK_NODE_SHIFT) as usize
    }

    /// Acquire read access to the node block
    pub fn read(&self) -> NodeBlockReader<'_> {
        NodeBlockReader {
            lock: self.data.read(),
        }
    }

    /// Acquire write access to the node block
    pub fn write(&self) -> NodeBlockWriter<'_> {
        NodeBlockWriter {
            lock: self.data.write(),
        }
    }

    /// Read only node access without keeping lock, data returned by copy
    pub fn node(&self, node_index: usize) -> Node {
        *self.read().node(node_index)
    }

    /// Read only node access without keeping lock, data returned by clone
    pub fn node_name_clone(&self, node_index: usize) -> Result<String, NodeNameError> {
        let guard = self.data.read();
        let node = &guard.data.node[node_index];
        let offset = node.name_offset as usize;
        let length = node.name_length as usize;
        if guard.name.len() < (offset + length) {
            return Err(InvalidArguments {
                reason: format!(
                    "node name offset {offset} + length {length} exceeds name table size {}",
                    guard.name.len()
                ),
            }
            .into());
        }
        let name =
            std::str::from_utf8(&guard.name[offset..(offset + length)]).internal("Node name")?;
        validate_node_name(name)?;
        Ok(name.to_string())
    }

    /// Returns the node name as a lock guard that holds a read lock on the block data.
    /// The returned `NodeNameLock` keeps the `RwLock` read guard alive for its lifetime.
    /// Callers must drop the returned value before calling `write()` on the same block,
    /// or the write lock acquisition will deadlock.
    pub fn node_name_ref(&self, node_index: usize) -> Result<NodeNameLock, NodeNameError> {
        let guard = self.data.read_arc();
        let node = &guard.data.node[node_index];
        let offset = node.name_offset as usize;
        let length = node.name_length as usize;
        if guard.name.len() < (offset + length) {
            return Err(InvalidArguments {
                reason: format!(
                    "node name offset {offset} + length {length} exceeds name table size {}",
                    guard.name.len()
                ),
            }
            .into());
        }
        let name_str =
            std::str::from_utf8(&guard.name[offset..(offset + length)]).internal("Node name")?;
        validate_node_name(name_str)?;
        let name: *const str = name_str;
        Ok(NodeNameLock {
            name,
            _guard: guard,
        })
    }

    pub async fn deserialize_nametable(
        &self,
        repository: Arc<RepositoryContext>,
    ) -> Result<(), StateError> {
        if self.is_nametable_deserialized() {
            return Ok(());
        }

        Box::pin(async move { self.deserialize_nametable_impl(repository).await }).await
    }

    async fn deserialize_nametable_impl(
        &self,
        repository: Arc<RepositoryContext>,
    ) -> Result<(), StateError> {
        let name_table = { self.read().lock.data.name_table };

        // Deserialize name table
        let bytes = immutable::read(
            repository.clone(),
            Address::zero_context_hash(name_table),
            None,
            immutable::read_options_from_repository(&repository)
                .with_cache()
                .with_priority()
                .with_max_content_size(NODE_NAME_MAX_SIZE as u64),
        )
        .await
        .internal("Deserialize deprecated name table failed")?;

        let nametable = bytes
            .try_into_mut()
            .unwrap_or_else(|bytes| BytesMut::from(&bytes[..]));

        let mut writer = self.write();
        if !self.is_nametable_deserialized() {
            writer.lock.name = nametable;
            self.name_deserialized.store(true, Ordering::Relaxed);
        }

        Ok(())
    }

    pub fn is_nametable_deserialized(&self) -> bool {
        self.name_deserialized.load(Ordering::Relaxed)
    }

    pub fn node_name_repack(&self) {
        let mut lock = self.write();
        lock.node_name_repack();
    }

    pub async fn deserialize(
        repository: Arc<RepositoryContext>,
        state: &State,
        address: Address,
    ) -> Result<NodeBlock, StateError> {
        // Normal path, expect latest data format version
        if let Ok(mut block_data) =
            NodeBlockData::read_box_from_immutable(repository.clone(), address, true).await
        {
            if block_data.version > NodeBlockFormat::Nametable as u32 {
                return Err(StateError::internal(format!(
                    "New revision state node block format encountered, please update your urc client: version {}",
                    block_data.version
                )));
            }
            block_data.flags &= !NodeBlockFlags::Dirty;
            block_data.flags &= !NodeBlockFlags::DeferRepackNametable;
            Ok(NodeBlock::new(block_data))
        } else {
            Self::deserialize_other_version(repository, state, address).await
        }
    }

    pub async fn deserialize_other_version(
        repository: Arc<RepositoryContext>,
        state: &State,
        address: Address,
    ) -> Result<NodeBlock, StateError> {
        // Try load old data format
        if let Ok(block_data_v0) =
            NodeBlockDataV0::read_box_from_immutable(repository.clone(), address, true).await
        {
            lore_trace!("Converting v0 block data format when deserializing block");
            let (mut block_data, name) =
                Self::convert_block_v0(repository.clone(), state, block_data_v0).await?;

            block_data.flags &= !NodeBlockFlags::Dirty;
            block_data.flags &= !NodeBlockFlags::DeferRepackNametable;

            return Ok(NodeBlock::new_with_name(block_data, name));
        };

        let block_data_v2 =
            match NodeBlockDataV2::read_box_from_immutable(repository.clone(), address, true).await
            {
                Ok(data) => Ok(data),
                Err(err) => Err(err)
                    .internal("Deserialize node block failed")
                    .map_err(StateError::from),
            }?;

        lore_debug!("Converting v2 block data format when deserializing block");
        let mut block_data = Self::convert_block_v2(repository.clone(), block_data_v2)?;

        block_data.flags &= !NodeBlockFlags::Dirty;
        block_data.flags &= !NodeBlockFlags::DeferRepackNametable;

        Ok(NodeBlock::new(block_data))
    }

    pub async fn convert_block_v0(
        repository: Arc<RepositoryContext>,
        state: &State,
        block_data_old: Box<NodeBlockDataV0>,
    ) -> Result<(Box<NodeBlockData>, BytesMut), StateError> {
        const EXPECTED_NAME_LENGTH: usize = 16;
        let mut block_data = NodeBlockData::new_from_heap_zeroed();
        let mut name_buffer =
            BytesMut::with_capacity(EXPECTED_NAME_LENGTH * node::BLOCK_V0_NODE_COUNT);

        block_data.flags = block_data_old.flags | NodeBlockFlags::UpgradeGeneratedNametable;
        block_data.node_count = block_data_old.node_count;
        block_data.node_unused_count = block_data_old.node_unused_count;
        block_data.node_unused = block_data_old.node_unused;
        block_data.block_unused_next = block_data_old.block_unused_next;
        block_data.version = NodeBlockFormat::Nametable as u32;

        for (index, node_old) in block_data_old.node.iter().enumerate() {
            let node = &mut block_data.node[index];
            node.flags = node_old.flags;
            node.mode = node_old.mode;
            node.child = node_old.child_mtime_node;
            node.parent = node_old.parent;
            node.sibling = node_old.sibling;
            node.size = node_old.size;
            node.address = node_old.address;

            let name = if !node_old.has_external_name() {
                let slice = unsafe {
                    std::slice::from_raw_parts(
                        node_old.name_string.as_ptr(),
                        node_old.name_string[NODE_NAME_LIMIT] as usize,
                    )
                };

                std::str::from_utf8(slice)
                    .internal("Invalid UTF-8 string")?
                    .to_string()
            } else {
                let name_table = state.nametable(repository.clone()).await?;

                let case_hash =
                    u64::from_le_bytes(node_old.name_string[0..8].try_into().unwrap_or_default());
                let name_string = name_table.load(case_hash);
                if !name_string.is_empty() {
                    name_string.to_string()
                } else {
                    name_table.load(node_old.name_hash).to_string()
                }
            };

            node.name_offset = name_buffer.len() as u32;
            node.name_length = name.len() as u32;
            node.name_hash = hash::hash_string(name.as_str());

            name_buffer.extend_from_slice(name.as_bytes());
        }

        Ok((block_data, name_buffer))
    }

    pub fn convert_block_v2(
        _repository: Arc<RepositoryContext>,
        block_data_v2: Box<NodeBlockDataV2>,
    ) -> Result<Box<NodeBlockData>, StateError> {
        let mut block_data = NodeBlockData::new_from_heap_zeroed();

        block_data.flags = block_data_v2.flags;
        block_data.node_count = block_data_v2.node_count;
        block_data.node_unused_count = block_data_v2.node_unused_count;
        block_data.node_unused = block_data_v2.node_unused;
        block_data.block_unused_next = block_data_v2.block_unused_next;
        block_data.name_table = block_data_v2.name_table;
        block_data.version = NodeBlockFormat::Nametable as u32;

        for (index, node_v2) in block_data_v2.node.iter().enumerate() {
            let node = &mut block_data.node[index];
            node.flags = 0;
            if node_v2.flags & NodeFlagsV2::File != 0 {
                node.flags |= NodeFlags::File;
            }
            if node_v2.flags & NodeFlagsV2::Link != 0 {
                node.flags |= NodeFlags::Link;
            }
            if node_v2.flags & NodeFlagsV2::Discarded != 0 {
                node.flags |= NodeFlags::Discarded;
            }
            if node_v2.flags & NodeFlagsV2::Dirty != 0 {
                node.flags |= NodeFlags::Dirty;
            }
            // NodeV1 staged bits
            // const StagedBits = 0b111111111110000;
            // NodeV2 staged bits
            // const StagedBits = 0b111111111110000000000000000;
            node.flags |= ((node_v2.flags & NodeFlagsV2::StagedBits) >> 12) as u16;
            if node_v2.flags & NodeFlagsV2::Executable != 0 {
                // Executable bit
                node.mode |= 0b1;
            }
            if node_v2.flags & NodeFlagsV2::File != 0 {
                node.child = 0;
            } else {
                node.child = node_v2.child;
            }
            node.parent = node_v2.parent;
            node.sibling = node_v2.sibling;
            node.size = node_v2.size;
            node.address = node_v2.address;
            node.name_offset = node_v2.name_offset;
            node.name_length = node_v2.name_length;
            node.name_hash = node_v2.name_hash;
        }

        Ok(block_data)
    }
}

impl NodeBlockReader<'_> {
    pub fn raw(&self) -> &NodeBlockData {
        self.lock.data.as_ref()
    }

    pub fn node_count(&self) -> usize {
        self.lock.data.node_count as usize
    }

    /// Get the node for the given node index
    pub fn node(&self, node_index: usize) -> &Node {
        debug_assert!(node_index < BLOCK_NODE_COUNT);
        &self.lock.data.node[node_index]
    }

    pub fn clone_name_table(&self) -> Bytes {
        self.lock.name.clone().freeze()
    }

    /// Access the full node block
    pub fn node_block(&self) -> &NodeBlockData {
        &self.lock.data
    }

    /// Check if block is full
    pub fn is_full(&self) -> bool {
        self.lock.data.node_unused_count == 0
            && self.lock.data.node_count == BLOCK_NODE_COUNT as u32
    }

    pub fn is_node_in_use(&self, node_index: usize) -> bool {
        if node_index >= self.lock.data.node_count as usize {
            return false;
        }

        // Check if one of the unused nodes
        let mut unused_count = self.lock.data.node_unused_count;
        let mut unused = self.lock.data.node_unused;
        while unused != INVALID_NODE && unused_count > 0 {
            let unused_index = Node::index(unused);
            if unused_index == node_index {
                return false;
            }
            unused = self.lock.data.node[unused_index].sibling;
            unused_count -= 1;
        }

        true
    }
}

impl NodeBlockWriter<'_> {
    pub fn node_count(&self) -> usize {
        self.lock.data.node_count as usize
    }

    /// Get mutable reference to node for the given node index
    pub fn node(&mut self, node_index: usize) -> &mut Node {
        debug_assert!(node_index < BLOCK_NODE_COUNT);
        &mut self.lock.data.node[node_index]
    }

    /// Mark the block as dirty
    pub fn mark_dirty(&mut self) -> bool {
        let block = &mut self.lock.data;
        let was_dirty = block.flags & NodeBlockFlags::Dirty != 0;
        block.flags |= NodeBlockFlags::Dirty;
        !was_dirty
    }

    pub fn discard_node(&mut self, block_index: usize, node_index: usize) {
        let block = &mut self.lock.data;
        if block.node_unused_count == 0 && block.node_count == BLOCK_NODE_COUNT as u32 {
            block.flags |= NodeBlockFlags::FirstUnusedNode;
        }
        {
            let node = &mut block.node[node_index];
            node.child = 0;
            node.address = Address::default();
            node.flags = NodeFlags::Discarded.bits();
            node.sibling = if block.node_unused_count > 0 {
                block.node_unused
            } else {
                INVALID_NODE
            };
        }
        block.node_unused = node_index as u32;
        block.node_unused_count += 1;
        // Keep the name for the discarded node for history weaving
        block.flags |= NodeBlockFlags::DeferRepackNametable;

        // First block root node is never counted as used in order to let that block reset
        // when all other nodes are unused.
        let node_used_count = block.node_count - if block_index == 0 { 1 } else { 0 };
        if node_used_count == block.node_unused_count {
            block.node_unused = 0;
            block.node_unused_count = 0;
            block.node_count = if block_index == 0 { 1 } else { 0 };
        }
    }

    /// Next unused block index
    pub fn grab_node_unused(&mut self, block_index: u32) -> u32 {
        let block = &mut self.lock.data;
        if block.node_unused_count > 0 {
            let node_index = Node::index(block.node_unused);
            let node = (block_index << BLOCK_NODE_SHIFT) | (node_index as u32);
            lore_trace!("Grab unused node {node_index} from block {block_index}");
            block.node_unused = block.node[node_index].sibling;
            block.node_unused_count -= 1;
            block.node[node_index].zero();
            node
        } else if block.node_count < BLOCK_NODE_COUNT as u32 {
            let node_index = block.node_count as usize;
            let node = (block_index << BLOCK_NODE_SHIFT) | (node_index as u32);
            lore_trace!("Grab uninitialized node {node_index} from block {block_index}");
            block.node_count += 1;
            block.node[node_index].zero();
            node
        } else {
            INVALID_NODE
        }
    }

    /// Get next block with unused nodes
    pub fn block_unused_next(&self) -> u32 {
        self.lock.data.block_unused_next
    }

    /// Check if block is full
    pub fn is_full(&self) -> bool {
        self.lock.data.node_unused_count == 0
            && self.lock.data.node_count == BLOCK_NODE_COUNT as u32
    }

    /// Access the full node block
    pub fn node_block(&mut self) -> &mut NodeBlockData {
        self.lock.data.as_mut()
    }

    pub fn node_name_store(
        &mut self,
        name: &str,
        prev_offset: u32,
        prev_length: u32,
    ) -> Result<(u32, u32), NodeNameError> {
        let prev_offset = prev_offset as usize;
        let prev_length = prev_length as usize;
        if name.len() <= prev_length && (prev_offset + prev_length) < self.lock.name.len() {
            // Safety: With the range check above this is safe, the buffer is guaranteed
            //         to have enough space for the memory copy
            unsafe {
                let offset = prev_offset as isize;
                std::ptr::copy_nonoverlapping(
                    name.as_ptr(),
                    self.lock.name.as_mut_ptr().offset(offset),
                    name.len(),
                );
            };
            Ok((prev_offset as u32, name.len() as u32))
        } else {
            let new_len = self.lock.name.len().saturating_add(name.len());
            if new_len > NODE_NAME_MAX_SIZE {
                return Err(NodeNameError::from(Oversized {
                    context: format!(
                        "node name store would grow to {new_len} bytes, exceeds \
                         NODE_NAME_MAX_SIZE {NODE_NAME_MAX_SIZE}"
                    ),
                }));
            }
            let offset = self.lock.name.len() as u32;
            self.lock.name.extend_from_slice(name.as_bytes());
            self.lock.data.flags |= NodeBlockFlags::RepackNametable;
            Ok((offset, name.len() as u32))
        }
    }

    pub fn node_name_repack(&mut self) {
        let lock = &mut self.lock;
        if lock.data.flags & NodeBlockFlags::RepackNametable == 0
            || lock.data.flags & NodeBlockFlags::DeferRepackNametable != 0
        {
            return;
        }

        let old_len = lock.name.len();
        if lock.name.is_empty() {
            return;
        }
        let mut new_name = BytesMut::with_capacity(lock.name.capacity());

        let node_count = lock.data.node_count as usize;
        for node_index in 0..node_count {
            let (offset, length) = {
                let node = &lock.data.node[node_index];
                if node.flags & NodeFlags::Discarded != 0 {
                    continue;
                }
                (node.name_offset as usize, node.name_length as usize)
            };

            if old_len >= (offset + length) {
                lock.data.node[node_index].name_offset = new_name.len() as u32;

                let name =
                    unsafe { std::str::from_utf8_unchecked(&lock.name[offset..(offset + length)]) };
                new_name.extend_from_slice(name.as_bytes());
            }
        }

        lock.name = new_name;
        lock.data.flags |= NodeBlockFlags::Dirty;
        lock.data.flags &= !NodeBlockFlags::RepackNametable;
    }
}

#[repr(C)]
#[derive(Debug, Clone, Copy, Default, FromBytes, IntoBytes, Immutable)]
pub struct NodeLink {
    /// Node identifier
    pub node: NodeID,
    /// Repository
    pub repository: RepositoryId,
    /// Revision signature
    pub revision: Hash,
}

impl NodeLink {
    pub fn is_valid(&self) -> bool {
        self.node.is_valid_node_id()
    }

    pub fn is_valid_or_root(&self) -> bool {
        self.node == ROOT_NODE || self.node.is_valid_node_id()
    }

    pub fn invalid() -> Self {
        NodeLink {
            node: INVALID_NODE,
            ..Default::default()
        }
    }

    pub async fn resolve(
        &self,
        repository: Arc<RepositoryContext>,
        state: Arc<State>,
    ) -> Result<(Arc<RepositoryContext>, Arc<State>), StateError> {
        if self.is_valid_or_root()
            && (repository.id != self.repository || state.revision() != self.revision)
        {
            let repository = Arc::new(repository.to_link_context(self.repository).await);
            let state = State::deserialize(repository.clone(), self.revision).await?;
            Ok((repository, state))
        } else {
            Ok((repository, state))
        }
    }
}

/// Delta entry
#[repr(C)]
#[derive(Debug, Clone, Copy, Default, IntoBytes, FromBytes, Immutable)]
pub struct NodeDelta {
    /// Node identifier
    pub node: NodeID,
    /// Unused
    pub _unused: u32,
    /// File action
    pub action: u16,
    /// Change flags
    pub flags: u16,
}

impl NodeDelta {
    pub fn from_node_and_flags(node: NodeID, node_flags: u16) -> Self {
        let mut change_flags = change::Flags::None;
        if node_flags & NodeFlags::StagedModify == NodeFlags::StagedModify {
            change_flags |= change::Flags::Modify;
        }
        if node_flags & NodeFlags::StagedMerge == NodeFlags::StagedMerge {
            change_flags |= change::Flags::Merge;
        }

        NodeDelta {
            node,
            _unused: 0,
            action: change::FileAction::from_node_flags(node_flags) as u16,
            flags: change_flags.bits(),
        }
    }

    pub fn from_node_change(change: NodeChange) -> Self {
        let node = match change.action {
            FileAction::Delete => change.from.node,
            FileAction::Add | FileAction::Move | FileAction::Copy | FileAction::Keep => {
                change.to.node
            }
        };
        NodeDelta {
            node,
            _unused: 0,
            action: change.action as u16,
            flags: change.flags.bits(),
        }
    }
}

/// File metadata for a node, 128 bytes
#[repr(C)]
#[derive(Clone, Copy, Debug, IntoBytes, FromBytes, Immutable)]
pub struct NodeFileMetadata {
    /// Custom metadata blob
    pub metadata: Hash,
    /// Repository ID in the cases where the file link can be from an external repository.
    /// For copy/move this is the repository for first revision
    /// For merge this is the repository for second revision
    pub repository: RepositoryId,
    /// Revision signatures
    pub revision: [Hash; 2],
    /// Node index in revision tree
    pub node: [u32; 2],
    /// File action type (copy/move/merge/add/delete/modify)
    pub action: [u16; 2],
    /// Additional flags
    pub flags: [u16; 2],
}

bitflags! {
    #[repr(transparent)]
    #[derive(Debug, Clone, Copy, PartialEq, Eq)]
    pub struct NodeFileMetadataFlags: u16 {
        /// No flags
        const NoFlag = 0;
    }
}
bitflagsops!(NodeFileMetadataFlags, u16);

bitflags! {
    #[repr(transparent)]
    #[derive(Debug, Clone, Copy, PartialEq, Eq)]
    pub struct NodeFileMetadataBlockFlags: u32 {
        /// Node block is dirty
        const Dirty = 0b1;
    }
}
bitflagsops!(NodeFileMetadataBlockFlags, u32);

impl NodeFileMetadata {
    const fn index_clamp(index: u32) -> u32 {
        if index < BLOCK_NODE_FILE_METADATA_COUNT as u32 {
            index
        } else {
            0
        }
    }

    /// Get the node index from a full node ID
    pub fn index(node: NodeID) -> usize {
        Self::index_clamp(node & NODE_INDEX_MASK) as usize
    }
}

pub const BLOCK_NODE_FILE_METADATA_COUNT: usize = BLOCK_NODE_COUNT;

/// Old block count before the metadata block was extended to 512 elements
const BLOCK_NODE_FILE_METADATA_COUNT_V0: usize = 511;

/// Block of file metadata, 65568 bytes, 32 bytes metadata, 512 blocks of 128 bytes each
#[repr(C)]
#[derive(Clone, Copy, IntoBytes, FromBytes, Immutable)]
pub struct NodeFileMetadataBlockData {
    /// Block flags
    pub flags: u32,
    /// Data format version
    pub version: u32,
    /// Reserved for future extensions, must be zero
    _reserved: [u32; 6],
    /// Node data
    pub node: [NodeFileMetadata; BLOCK_NODE_FILE_METADATA_COUNT],
}

impl ReadBoxFromImmutable for NodeFileMetadataBlockData {}
impl ZeroHeapAlloc for NodeFileMetadataBlockData {}

/// Legacy block of file metadata with 511 elements (old format before extension to 512)
#[repr(C)]
#[derive(Clone, Copy, IntoBytes, FromBytes, Immutable)]
struct NodeFileMetadataBlockDataV0 {
    flags: u32,
    version: u32,
    _reserved: [u32; 6],
    node: [NodeFileMetadata; BLOCK_NODE_FILE_METADATA_COUNT_V0],
}

impl ReadBoxFromImmutable for NodeFileMetadataBlockDataV0 {}
impl ZeroHeapAlloc for NodeFileMetadataBlockDataV0 {}

impl NodeFileMetadataBlockDataV0 {
    /// Convert the old 511-element block into the current 512-element format.
    /// The last element is zero-initialized.
    fn into_current(self) -> Box<NodeFileMetadataBlockData> {
        let mut block = NodeFileMetadataBlockData::new_from_heap_zeroed();
        block.flags = self.flags;
        block.version = self.version;
        block.node[..BLOCK_NODE_FILE_METADATA_COUNT_V0].copy_from_slice(&self.node);
        block
    }
}

impl NodeFileMetadataBlockData {
    /// Read from immutable store with fallback for legacy 511-element blocks.
    pub async fn read_box_from_immutable_compat(
        repository: Arc<RepositoryContext>,
        address: Address,
        cache: bool,
    ) -> Result<Box<NodeFileMetadataBlockData>, ImmutableError> {
        match NodeFileMetadataBlockData::read_box_from_immutable(repository.clone(), address, cache)
            .await
        {
            Ok(block) => Ok(block),
            Err(original_err) if original_err.is_internal() => {
                match NodeFileMetadataBlockDataV0::read_box_from_immutable(
                    repository, address, cache,
                )
                .await
                {
                    Ok(old_block) => Ok(old_block.into_current()),
                    Err(_) => Err(original_err),
                }
            }
            Err(e) => Err(e),
        }
    }
}

/// Internally mutable wrapper around a block of nodes
pub struct NodeFileMetadataBlock {
    /// Node block data containing all the nodes
    data: parking_lot::RwLock<Box<NodeFileMetadataBlockData>>,
}

/// Read accessor for a node block
pub struct NodeFileMetadataBlockReader<'a> {
    lock: RwLockReadGuard<'a, Box<NodeFileMetadataBlockData>>,
}

/// Write accessor for a node block
pub struct NodeFileMetadataBlockWriter<'a> {
    lock: RwLockWriteGuard<'a, Box<NodeFileMetadataBlockData>>,
}

impl Default for NodeFileMetadataBlock {
    fn default() -> Self {
        Self::new(NodeFileMetadataBlockData::new_from_heap_zeroed())
    }
}

impl NodeFileMetadataBlock {
    pub fn new(data: Box<NodeFileMetadataBlockData>) -> Self {
        NodeFileMetadataBlock {
            data: RwLock::new(data),
        }
    }

    /// Get the block index from a full node ID
    pub fn index(node: NodeID) -> usize {
        (node >> BLOCK_NODE_SHIFT) as usize
    }

    pub fn read(&self) -> NodeFileMetadataBlockReader<'_> {
        NodeFileMetadataBlockReader {
            lock: self.data.read(),
        }
    }

    pub fn write(&self) -> NodeFileMetadataBlockWriter<'_> {
        NodeFileMetadataBlockWriter {
            lock: self.data.write(),
        }
    }

    pub fn node(&self, node_index: usize) -> NodeFileMetadata {
        *self.read().node(node_index)
    }
}

impl NodeFileMetadataBlockReader<'_> {
    /// Get the node for the given node index
    pub fn node(&self, node_index: usize) -> &NodeFileMetadata {
        debug_assert!(node_index < BLOCK_NODE_COUNT);
        &self.lock.node[node_index]
    }

    /// Access the full node block
    pub fn node_block(&self) -> &NodeFileMetadataBlockData {
        &self.lock
    }
}

impl NodeFileMetadataBlockWriter<'_> {
    /// Get the node for the given node index
    pub fn node(&mut self, node_index: usize) -> &mut NodeFileMetadata {
        debug_assert!(node_index < BLOCK_NODE_COUNT);
        &mut self.lock.node[node_index]
    }

    /// Mark the block as dirty
    pub fn mark_dirty(&mut self) -> bool {
        let was_dirty = self.lock.flags & NodeFileMetadataBlockFlags::Dirty != 0;
        self.lock.flags |= NodeFileMetadataBlockFlags::Dirty;
        !was_dirty
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    fn node_with_flags(flags: u16) -> Node {
        Node {
            flags,
            ..Default::default()
        }
    }

    #[test]
    fn dirty_flag_bit_positions() {
        // V1: Dirty at bit 3
        assert_eq!(NodeFlags::Dirty.bits(), 0b1000);
        // V1: Dirty does not overlap with Staged (bit 4)
        assert_eq!(NodeFlags::Dirty.bits() & NodeFlags::Staged.bits(), 0);
        // V1: Dirty does not overlap with File/Module/ExternalName
        assert_eq!(NodeFlags::Dirty.bits() & NodeFlags::File.bits(), 0);
        assert_eq!(NodeFlags::Dirty.bits() & NodeFlags::Link.bits(), 0);

        // V2: Dirty at bit 15
        assert_eq!(NodeFlagsV2::Dirty.bits(), 1 << 15);
        // V2: Dirty does not overlap with Staged (bit 16)
        assert_eq!(NodeFlagsV2::Dirty.bits() & NodeFlagsV2::Staged.bits(), 0);
    }

    #[test]
    fn dirty_compound_flags_v1() {
        assert_eq!(
            NodeFlags::DirtyModify.bits(),
            NodeFlags::Dirty.bits() | NodeFlags::StagedModify.bits() & NodeFlags::ActionBits.bits()
        );
        assert_eq!(
            NodeFlags::DirtyAdd.bits(),
            NodeFlags::Dirty.bits() | NodeFlags::StagedAdd.bits() & NodeFlags::ActionBits.bits()
        );
        assert_eq!(
            NodeFlags::DirtyDelete.bits(),
            NodeFlags::Dirty.bits() | NodeFlags::StagedDelete.bits() & NodeFlags::ActionBits.bits()
        );
        assert_eq!(
            NodeFlags::DirtyMove.bits(),
            NodeFlags::Dirty.bits() | NodeFlags::StagedMove.bits() & NodeFlags::ActionBits.bits()
        );
        assert_eq!(
            NodeFlags::DirtyCopy.bits(),
            NodeFlags::Dirty.bits() | NodeFlags::StagedCopy.bits() & NodeFlags::ActionBits.bits()
        );
    }

    #[test]
    fn dirty_bits_mask() {
        // DirtyBits = Dirty + ActionBits (bits 3, 5-9)
        let expected = NodeFlags::Dirty.bits() | NodeFlags::ActionBits.bits();
        assert_eq!(NodeFlags::DirtyBits.bits(), expected);
        // DirtyBits does NOT include Staged (bit 4)
        assert_eq!(NodeFlags::DirtyBits.bits() & NodeFlags::Staged.bits(), 0);
        // DirtyBits does NOT include MergeBits
        assert_eq!(NodeFlags::DirtyBits.bits() & NodeFlags::MergeBits.bits(), 0);
    }

    #[test]
    fn action_bits_mask() {
        // ActionBits = bits 5-9 (shared between Dirty and Staged)
        let expected = (NodeFlags::StagedModify.bits()
            | NodeFlags::StagedAdd.bits()
            | NodeFlags::StagedDelete.bits()
            | NodeFlags::StagedMove.bits()
            | NodeFlags::StagedCopy.bits())
            & !NodeFlags::Staged.bits();
        assert_eq!(NodeFlags::ActionBits.bits(), expected);
    }

    #[test]
    fn node_is_dirty_queries() {
        // Clean node
        let node = Node::default();
        assert!(!node.is_dirty());
        assert!(!node.is_dirty_modify());

        // Dirty only
        let node = node_with_flags(NodeFlags::DirtyModify.bits());
        assert!(node.is_dirty());
        assert!(node.is_dirty_modify());
        assert!(!node.is_dirty_add());
        assert!(!node.is_staged());

        // Dirty+Staged (orthogonal)
        let node = node_with_flags(NodeFlags::Dirty.bits() | NodeFlags::StagedModify.bits());
        assert!(node.is_dirty());
        assert!(node.is_staged());
        assert!(node.is_staged_modify());
        assert!(node.is_dirty_or_staged());
    }

    #[test]
    fn node_has_any_change_flags() {
        assert!(!Node::default().has_any_change_flags());
        assert!(node_with_flags(NodeFlags::Dirty.bits()).has_any_change_flags());
        assert!(node_with_flags(NodeFlags::Staged.bits()).has_any_change_flags());
        assert!(!node_with_flags(NodeFlags::File.bits()).has_any_change_flags());
    }

    #[test]
    fn clear_staged_flags_preserves_dirty() {
        let mut node = node_with_flags(NodeFlags::Dirty.bits() | NodeFlags::StagedModify.bits());
        node.clear_staged_flags();
        assert!(node.is_dirty());
        assert!(!node.is_staged());
        assert_ne!(node.flags & NodeFlags::ActionBits.bits(), 0);
    }

    #[test]
    fn clear_staged_flags_clears_action_when_no_dirty() {
        let mut node = node_with_flags(NodeFlags::StagedModify.bits());
        node.clear_staged_flags();
        assert_eq!(
            node.flags & (NodeFlags::StagedBits.bits() | NodeFlags::Dirty.bits()),
            0
        );
    }

    #[test]
    fn clear_dirty_flags_preserves_staged() {
        let mut node = node_with_flags(NodeFlags::Dirty.bits() | NodeFlags::StagedModify.bits());
        node.clear_dirty_flags();
        assert!(!node.is_dirty());
        assert!(node.is_staged());
        assert!(node.is_staged_modify());
    }

    #[test]
    fn clear_dirty_flags_clears_action_when_no_staged() {
        let mut node = node_with_flags(NodeFlags::DirtyModify.bits());
        node.clear_dirty_flags();
        assert_eq!(
            node.flags & (NodeFlags::DirtyBits.bits() | NodeFlags::StagedBits.bits()),
            0
        );
    }

    #[test]
    fn clear_all_change_flags() {
        let mut node = node_with_flags(
            NodeFlags::File.bits() | NodeFlags::Dirty.bits() | NodeFlags::StagedModify.bits(),
        );
        node.clear_all_change_flags();
        assert!(node.is_file());
        assert!(!node.is_dirty());
        assert!(!node.is_staged());
        assert_eq!(node.flags & NodeFlags::ActionBits.bits(), 0);
    }

    #[test]
    fn action_bits_extraction() {
        let node = node_with_flags(NodeFlags::DirtyMove.bits());
        assert_eq!(
            node.action_bits(),
            NodeFlags::StagedMove.bits() & NodeFlags::ActionBits.bits()
        );
    }
}
