/// A node in the revision tree, 128 bytes (32 bit index, max 4G nodes)
#[repr(C)]
#[derive(Debug, Copy, Clone, IntoBytes, FromBytes, Immutable)]
pub struct NodeV0 {
    /// Flags
    pub flags: u16,
    /// File - Executable flag in first bit
    pub mode: u16,
    /// File - Last modified time high 16 bits
    pub mtime_high: u32,
    /// Directory - first child index
    /// File - Last modified time
    /// Submodule - Target tree node
    pub child_mtime_node: u32,
    /// Parent index
    pub parent: u32,
    /// Next sibling index
    pub sibling: u32,
    /// Name string (last byte is length)
    pub name_string: [u8; NODE_NAME_LIMIT + 1],
    /// Name hash (case insensitive)
    pub name_hash: u64,
    /// File size
    pub size: u64,
    /// Node content address
    /// - For files this is the content payload (content hash and file ID).
    /// - For directories the hash of all child node content hashes (sorted by node name hash in ascending order).
    /// - For a submodule this is the target revision hash and repository ID.
    pub address: Address,
}

impl NodeV0 {
    /// Check if node is staged for inclusion in a commit
    pub fn is_staged(&self) -> bool {
        (self.flags & NodeFlags::Staged) == NodeFlags::Staged.bits()
    }

    /// Check if node is staged for deletion
    pub fn is_staged_delete(&self) -> bool {
        (self.flags & NodeFlags::StagedDelete) == NodeFlags::StagedDelete.bits()
    }

    /// Check if node is staged for add
    pub fn is_staged_add(&self) -> bool {
        (self.flags & NodeFlags::StagedAdd) == NodeFlags::StagedAdd.bits()
    }

    /// Check if node is staged for modify
    pub fn is_staged_modify(&self) -> bool {
        (self.flags & NodeFlags::StagedModify) == NodeFlags::StagedModify.bits()
    }

    /// Check if node is staged for move
    pub fn is_staged_move(&self) -> bool {
        (self.flags & NodeFlags::StagedMove) == NodeFlags::StagedMove.bits()
    }

    /// Check if node is staged for copy
    pub fn is_staged_copy(&self) -> bool {
        (self.flags & NodeFlags::StagedCopy) == NodeFlags::StagedCopy.bits()
    }

    /// Check if node is part of a merge
    pub fn is_staged_merge(&self) -> bool {
        (self.flags & NodeFlags::StagedMerge) == NodeFlags::StagedMerge.bits()
    }

    /// Check if node is part of a conflict - either resolved or unresolved
    pub fn is_staged_merge_conflict(&self) -> bool {
        (self.flags & NodeFlags::StagedMergeConflict) == NodeFlags::StagedMergeConflict.bits()
    }

    /// Check if node is part of a conflict - resolved
    pub fn is_staged_merge_resolved(&self) -> bool {
        (self.flags & NodeFlags::StagedMergeResolved) == NodeFlags::StagedMergeResolved.bits()
    }

    /// Check if node is part of a conflict - unresolved
    pub fn is_staged_merge_unresolved(&self) -> bool {
        self.is_staged_merge_conflict() && !self.is_staged_merge_resolved()
    }

    /// Check if node is part of a conflict and the mine version got staged
    pub fn is_staged_merge_mine(&self) -> bool {
        (self.flags & NodeFlags::StagedMergeMine) == NodeFlags::StagedMergeMine.bits()
    }

    /// Check if node is part of a conflict and the theirs version got staged
    pub fn is_staged_merge_theirs(&self) -> bool {
        (self.flags & NodeFlags::StagedMergeTheirs) == NodeFlags::StagedMergeTheirs.bits()
    }

    /// Check if the node is a file
    pub fn is_file(&self) -> bool {
        (self.flags & NodeFlags::File) == NodeFlags::File.bits()
    }

    /// Check if the node is a link
    pub fn is_link(&self) -> bool {
        (self.flags & NodeFlags::Link) == NodeFlags::Link.bits()
    }

    /// Check if the node is a directory
    pub fn is_directory(&self) -> bool {
        !self.is_file() && !self.is_link()
    }

    /// Check if node is marked as discarded
    pub fn is_discarded(&self) -> bool {
        (self.flags & NodeFlags::Discarded) == NodeFlags::Discarded.bits()
    }

    /// Check if the node has an external name string
    pub fn has_external_name(&self) -> bool {
        NodeFlags::from_bits_retain(self.flags).contains(NodeFlags::ExternalNameDeprecated)
    }

    const fn index_clamp(index: u32) -> u32 {
        if index < BLOCK_NODE_COUNT as u32 {
            index
        } else {
            0
        }
    }

    /// Get the node index from a full node ID
    pub fn index(node: NodeID) -> usize {
        Self::index_clamp(node & NODE_INDEX_MASK) as usize
    }

    /// Get the first child node (only for directory nodes)
    pub fn child(&self) -> Option<NodeID> {
        debug_assert!(self.is_directory());
        let node = self.child_mtime_node as NodeID;
        if node.is_valid_node_id() {
            Some(node)
        } else {
            None
        }
    }

    /// Get the sibling of this node
    pub fn sibling(&self) -> Option<NodeID> {
        let node = self.sibling as NodeID;
        if node.is_valid_node_id() {
            Some(node)
        } else {
            None
        }
    }

    /// Get the last modified time (only for file nodes)
    pub fn mtime(&self) -> u64 {
        debug_assert!(self.is_file());
        ((self.mtime_high as u64) << 32u64) | (self.child_mtime_node as u64)
    }

    /// Get submodule node link (only for module nodes)
    pub fn linked_node(&self) -> NodeLink {
        debug_assert!(self.is_link());
        NodeLink {
            node: self.child_mtime_node,
            repository: self.address.context.into(),
            revision: self.address.hash,
        }
    }
}

